/*Tipos diferentes de funções*/

function dizerNome() {
    console.log('Rafael');
}

dizerNome(); // a primeira realiza uma tarefa e não devolve nada //


function multiplicarPorDois (valor) {

    return valor* 2;
}

console.log(multiplicarPorDois(3)); //o segundo tipo realiza uma tarefa e nos retorna um valor,mais exemplos abaixo



function multiplicarPorDez(valor) {
    return valor*10;
}



let resultado = multiplicarPorDez(10);
