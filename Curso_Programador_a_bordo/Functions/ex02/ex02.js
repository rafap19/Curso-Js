/* Podemos ainda criar funções com parâmetros */

function escrevaMensagem(mensagem) {
    console.log(mensagem);
}

escrevaMensagem('Hello World');
escrevaMensagem('Deus é bom')

/* Neste caso dando um parâmetro mensagem para a função,determinamos que console.log irá executar esse parâmetro, isso faz com que por exemplo quando chamarmos a função vamos poder escrever oque quiser,visto que o parâmetro mensagem é determinado pelo console.log,ou seja ao invés de aparecer uma frase escrita ele nos da autonomia de escrever oque quiser,temos então uma função customizável. */